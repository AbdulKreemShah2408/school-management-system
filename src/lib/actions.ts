export const createStudent = async (
  currentState: CurrentState,
  data: StudentSchema
) => {
  console.log(data);
  try {
    const classItem = await prisma.class.findUnique({
      where: { id: data.classId },
      include: { _count: { select: { students: true } } },
    });

    if (classItem && classItem.capacity === classItem._count.students) {
      return { success: false, error: true };
    }

    const user = await clerkClient.users.createUser({
      username: data.username,
      emailAddress: [data.email || ""],
      password: data.password,
      firstName: data.name,
      lastName: data.surname,
      publicMetadata:{role:"student"}
    });

    await prisma.student.create({
      data: {
        id: user.id,
        username: data.username,
        name: data.name,
        surname: data.surname,
        email: data.email || null,
        phone: data.phone || null,
        address: data.address,
        img: data.img || null,
        bloodType: data.bloodType,
        sex: data.sex,
        birthday: data.birthday,
        gradeId: data.gradeId,
        classId: data.classId,
        parentId: data.parentId,
      },
    });

    // revalidatePath("/list/students");
    return { success: true, error: false };
  } catch (err) {
    console.log(err);
    return { success: false, error: true };
  }
};

export const updateStudent = async (
  currentState: CurrentState,
  data: StudentSchema
) => {
  if (!data.id) {
    return { success: false, error: true };
  }
  try {
    const userUpdate: any = {
      username: data.username,
      firstName: data.name,
      lastName: data.surname,
      emailAddress: [data.email || ""], // ✅ email add kiya teacher wale jese
    };

    if (data.password && data.password !== "") {
      userUpdate.password = data.password;
    }

    await clerkClient.users.updateUser(data.id, userUpdate);

    await prisma.student.update({
      where: { id: data.id },
      data: {
       
        username: data.username,
        name: data.name,
        surname: data.surname,
        email: data.email || null,
        phone: data.phone || null,
        address: data.address,
        img: data.img || null,
        bloodType: data.bloodType,
        sex: data.sex,
        birthday: data.birthday,
        gradeId: data.gradeId,
        classId: data.classId,
        parentId: data.parentId,
      },
    });

    return { success: true, error: false };
  } catch (err) {
    console.log(err);
    return { success: false, error: true };
  }
};


export const deleteStudent = async (
  currentState: CurrentState,
  data: FormData
) => {
  const id = data.get("id") as string;

  try {
    // 🔹 Pehle Clerk user check karo
    try {
      await clerkClient.users.getUser(id); // check existence
      await clerkClient.users.deleteUser(id); // agar mila to delete
    } catch (clerkErr: any) {
      if (clerkErr.status === 404) {
        console.log("Clerk student not found, skipping Clerk delete.");
      } else {
        throw clerkErr; // koi aur error to propagate kar do
      }
    }

    // 🔹 Ab Prisma student delete karo
    await prisma.student.delete({
      where: { id },
    });

    // revalidatePath("/list/students");
    return { success: true, error: false };
  } catch (err) {
    console.log(err);
    return { success: false, error: true };
  }
};

